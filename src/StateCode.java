public class StateCode {
    private int ten = 0;
    private int twoHund = 0;
    private int fourZeroFour = 0;

    public void addTen() {
        this.ten++;
    }

    public void addTwoHund() {
        this.twoHund++;
    }

    public void addFourZeroFour() {
        this.fourZeroFour++;
    }

    public int getTen() {
        return ten;
    }

    public int getTwoHund() {
        return twoHund;
    }

    public int getFourZeroFour() {
        return fourZeroFour;
    }
}
